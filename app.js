function bindEvents() {
  $$(".nav-btn").forEach((btn) => btn.addEventListener("click", () => switchPage(btn.dataset.page)));

  $("#chatForm").addEventListener("submit", async (event) => {
    event.preventDefault();
    const input = $("#chatInput");
    const submitButton = $("#chatForm button[type='submit']");
    const question = input.value.trim();
    if (!question) return;
    const userMsg = { id: uid(), role: "user", text: question, createdAt: nowIso() };
    state.chat.push(userMsg);
    renderMessage("user", question, []);
    input.value = "";
    if (submitButton) submitButton.disabled = true;

    try {
      const answer = await buildAnswer(question);
      const assistantMsg = {
        id: uid(),
        role: "assistant",
        text: answer.text,
        sources: answer.matches,
        confidence: answer.confidence,
        createdAt: nowIso()
      };
      state.chat.push(assistantMsg);
      save(STORE.chat, state.chat.slice(-80));

      const delay = state.settings.humanMode ? Math.min(950, 220 + answer.text.length * 4) : 120;
      setTimeout(() => renderMessage("assistant", answer.text, answer.matches), delay);
      renderDashboard();
    } finally {
      if (submitButton) submitButton.disabled = false;
      input.focus();
    }
  });

  $("#chatInput").addEventListener("keydown", (event) => {
    if ((event.ctrlKey || event.metaKey) && event.key === "Enter") {
      $("#chatForm").requestSubmit();
    }
  });

  $("#newChatBtn").addEventListener("click", () => {
    if (!state.chat.length || confirm("مێژووی چاتی ئێستا پاک بکرێتەوە؟")) {
      state.chat = [];
      save(STORE.chat, state.chat);
      renderChat();
      renderDashboard();
    }
  });

  $$("[data-prompt]").forEach((btn) => btn.addEventListener("click", () => {
    $("#chatInput").value = btn.dataset.prompt;
    $("#chatInput").focus();
  }));

  $("#loginForm").addEventListener("submit", async (event) => {
    event.preventDefault();
    const password = $("#adminPassword").value;
    const expected = localStorage.getItem(STORE.password);
    if (await sha256(password) === expected) {
      setAdmin(true);
      $("#adminPassword").value = "";
      toast("بە سەرکەوتوویی چوویتە ژوورەوە.");
    } else {
      toast("وشەی نهێنی هەڵەیە.");
    }
  });

  $("#knowledgeForm").addEventListener("submit", (event) => {
    event.preventDefault();
    if (!state.isAdmin) return toast("پێویستە بەڕێوەبەر داخڵ بێت.");
    const item = getFormData();
    if (!item.title || !item.question || !item.answer) return toast("ناونیشان، پرسیار و وەڵام پێویستن.");
    saveKnowledgeItem(item);
    clearForm();
    toast("زیرەکییەکە ئەم زانیارییەی فێر بوو.");
  });

  $("#resetFormBtn").addEventListener("click", clearForm);
  $("#smartFillBtn").addEventListener("click", smartFill);

  $("#testSearchBtn").addEventListener("click", () => {
    const query = $("#testQuery").value.trim();
    const rows = searchKnowledge(query, 7, false);
    $("#testResults").innerHTML = rows.length ? rows.map((row) => `
      <div class="source-card"><strong>${escapeHtml(row.item.title)}</strong><br><small>نمرە: ${Math.round(row.score)} | دڵنیایی: ${confidenceFromScore(row.score, row.coverage)} | ${escapeHtml((row.hits || []).join(", "))}</small></div>
    `).join("") : `<div class="source-card">هیچ گونجانێک نەدۆزرایەوە.</div>`;
  });

  $("#knowledgeSearch").addEventListener("input", renderKnowledge);
  $("#exportBtn").addEventListener("click", exportData);
  $("#importInput").addEventListener("change", (event) => {
    if (event.target.files[0]) importData(event.target.files[0]);
    event.target.value = "";
  });

  $("#saveSettingsBtn").addEventListener("click", () => {
    state.settings.botName = $("#botNameInput").value.trim() || DEFAULT_SETTINGS.botName;
    state.settings.threshold = Number($("#thresholdInput").value);
    state.settings.showSources = $("#sourceToggle").checked;
    if ($("#styleInput")) state.settings.style = $("#styleInput").value;
    if ($("#humanModeToggle")) state.settings.humanMode = $("#humanModeToggle").checked;
    if ($("#clarifyToggle")) state.settings.askClarification = $("#clarifyToggle").checked;
    if ($("#maxSourcesInput")) state.settings.maxSources = Number($("#maxSourcesInput").value);
    if ($("#autoPackToggle")) state.settings.autoDownloadGeneralPack = $("#autoPackToggle").checked;
    if ($("#autoInternetToggle")) state.settings.autoInternetBootstrap = $("#autoInternetToggle").checked;
    if ($("#internetTargetGbInput")) state.settings.internetBootstrapTargetBytes = Math.max(0.05, Math.min(2, Number($("#internetTargetGbInput").value || 2))) * 1024 * 1024 * 1024;
    if ($("#packManifestUrlInput")) state.settings.packManifestUrl = $("#packManifestUrlInput").value.trim() || DEFAULT_SETTINGS.packManifestUrl;
    if ($("#maxGeneralCandidatesInput")) state.settings.maxGeneralCandidates = Number($("#maxGeneralCandidatesInput").value);
    save(STORE.settings, state.settings);
    renderSettings();
    renderChat();
    toast("ڕێکخستنەکان پاشەکەوت کران.");
  });

  $("#thresholdInput").addEventListener("input", () => {
    $("#thresholdLabel").textContent = $("#thresholdInput").value;
  });
  if ($("#maxSourcesInput")) {
    $("#maxSourcesInput").addEventListener("input", () => {
      $("#maxSourcesLabel").textContent = $("#maxSourcesInput").value;
    });
  }
  if ($("#maxGeneralCandidatesInput")) {
    $("#maxGeneralCandidatesInput").addEventListener("input", () => {
      $("#maxGeneralCandidatesLabel").textContent = $("#maxGeneralCandidatesInput").value;
    });
  }
  if ($("#downloadPackBtn")) {
    $("#downloadPackBtn").addEventListener("click", async () => {
      if ($("#packManifestUrlInput")) state.settings.packManifestUrl = $("#packManifestUrlInput").value.trim() || DEFAULT_SETTINGS.packManifestUrl;
      save(STORE.settings, state.settings);
      await downloadAndInstallGeneralPack(state.settings.packManifestUrl, { force: true });
    });
  }
  if ($("#installBundledPackBtn")) {
    $("#installBundledPackBtn").addEventListener("click", async () => {
      await installEmbeddedGeneralPack({ force: true });
    });
  }
  if ($("#checkManifestBtn")) {
    $("#checkManifestBtn").addEventListener("click", async () => {
      if ($("#packManifestUrlInput")) state.settings.packManifestUrl = $("#packManifestUrlInput").value.trim() || DEFAULT_SETTINGS.packManifestUrl;
      save(STORE.settings, state.settings);
      await checkManifestConnection(state.settings.packManifestUrl);
    });
  }
  if ($("#internetBootstrapBtn")) {
    $("#internetBootstrapBtn").addEventListener("click", async () => {
      if ($("#autoInternetToggle")) state.settings.autoInternetBootstrap = $("#autoInternetToggle").checked;
      if ($("#internetTargetGbInput")) state.settings.internetBootstrapTargetBytes = Math.max(0.05, Math.min(2, Number($("#internetTargetGbInput").value || 2))) * 1024 * 1024 * 1024;
      save(STORE.settings, state.settings);
      await runInternetBootstrap({ force: true });
    });
  }
  if ($("#stopInternetBootstrapBtn")) {
    $("#stopInternetBootstrapBtn").addEventListener("click", stopInternetBootstrap);
  }
  if ($("#clearPackBtn")) {
    $("#clearPackBtn").addEventListener("click", async () => {
      if (!confirm("دڵنیایت دەتەوێت زانیاریی گشتیی هەڵگیراو بسڕیتەوە؟")) return;
      await clearGeneralPack();
      updatePackProgress("زانیاریی گشتی پاک کرایەوە.", 0, "");
      toast("زانیاریی گشتی پاک کرایەوە.");
    });
  }

  $("#changePasswordBtn").addEventListener("click", async () => {
    if (!state.isAdmin) return toast("پێویستە بەڕێوەبەر داخڵ بێت.");
    const next = $("#newPasswordInput").value;
    if (next.length < 6) return toast("وشەی نهێنی پێویستە بەلایەنی کەم ٦ پیت بێت.");
    localStorage.setItem(STORE.password, await sha256(next));
    $("#newPasswordInput").value = "";
    toast("وشەی نهێنی گۆڕدرا.");
  });

  $("#logoutBtn").addEventListener("click", () => {
    setAdmin(false);
    toast("چوویتە دەرەوە.");
  });

  $("#seedBtn").addEventListener("click", seedKnowledge);
  $("#clearChatBtn").addEventListener("click", () => {
    if (!confirm("دڵنیایت دەتەوێت مێژووی چات بسڕیتەوە؟")) return;
    state.chat = [];
    save(STORE.chat, state.chat);
    renderChat();
    renderDashboard();
    toast("مێژووی چات پاک کرایەوە.");
  });

  $("#wipeBtn").addEventListener("click", async () => {
    if (!state.isAdmin) return toast("پێویستە بەڕێوەبەر داخڵ بێت.");
    if (!confirm("ئەم کردارە هەموو زانیاری، چات، ڕێکخستنەکان و زانیاریی گشتی پاک دەکات. دڵنیایت؟")) return;
    localStorage.removeItem(STORE.knowledge);
    localStorage.removeItem(STORE.chat);
    localStorage.removeItem(STORE.settings);
    localStorage.removeItem(STORE.learningLog);
    state.knowledge = [];
    state.chat = [];
    state.settings = { ...DEFAULT_SETTINGS };
    cachedIndex = null;
    save(STORE.knowledge, state.knowledge);
    save(STORE.chat, state.chat);
    save(STORE.settings, state.settings);
    await clearGeneralPack();
    renderAll();
    toast("هەموو زانیارییەکان پاک کرانەوە.");
  });
}

function renderSettings() {
  $("#botNameInput").value = state.settings.botName;
  $("#thresholdInput").value = state.settings.threshold;
  $("#thresholdLabel").textContent = state.settings.threshold;
  $("#sourceToggle").checked = Boolean(state.settings.showSources);
  if ($("#styleInput")) $("#styleInput").value = state.settings.style || DEFAULT_SETTINGS.style;
  if ($("#humanModeToggle")) $("#humanModeToggle").checked = state.settings.humanMode !== false;
  if ($("#clarifyToggle")) $("#clarifyToggle").checked = state.settings.askClarification !== false;
  if ($("#maxSourcesInput")) {
    $("#maxSourcesInput").value = state.settings.maxSources || DEFAULT_SETTINGS.maxSources;
    $("#maxSourcesLabel").textContent = $("#maxSourcesInput").value;
  }
  if ($("#packManifestUrlInput")) $("#packManifestUrlInput").value = state.settings.packManifestUrl || DEFAULT_SETTINGS.packManifestUrl;
  if ($("#autoPackToggle")) $("#autoPackToggle").checked = state.settings.autoDownloadGeneralPack !== false;
  if ($("#autoInternetToggle")) $("#autoInternetToggle").checked = Boolean(state.settings.autoInternetBootstrap);
  if ($("#internetTargetGbInput")) $("#internetTargetGbInput").value = (Number(state.settings.internetBootstrapTargetBytes || DEFAULT_SETTINGS.internetBootstrapTargetBytes) / 1024 / 1024 / 1024).toFixed(2).replace(/\.00$/, "");
  if ($("#maxGeneralCandidatesInput")) {
    $("#maxGeneralCandidatesInput").value = state.settings.maxGeneralCandidates || DEFAULT_SETTINGS.maxGeneralCandidates;
    $("#maxGeneralCandidatesLabel").textContent = $("#maxGeneralCandidatesInput").value;
  }
  renderGeneralPackStatus();
}

function renderAll() {
  renderSettings();
  renderChat();
  renderKnowledge();
  renderDashboard();
  renderGeneralPackStatus();
  setAdmin(state.isAdmin);
}

function setupOfflineStatus() {
  const update = () => {
    $("#offlineStatus").textContent = navigator.onLine ? "کار دەکات؛ ئامادەی ئۆفلاین" : "ئێستا ئۆفلاینیت";
  };
  update();
  window.addEventListener("online", update);
  window.addEventListener("offline", update);
}

async function registerServiceWorker() {
  if ("serviceWorker" in navigator && location.protocol !== "file:") {
    try {
      await navigator.serviceWorker.register("service-worker.js");
    } catch (error) {
      console.info("Service worker unavailable", error);
    }
  }
}

async function init() {
  state.knowledge = normalizeImportedKnowledge(state.knowledge);
  save(STORE.knowledge, state.knowledge);
  await ensurePassword();
  state.generalStats = await getGeneralStats();
  bindEvents();
  setupOfflineStatus();
  renderAll();
  registerServiceWorker();
  autoBootstrapGeneralPack();
}

init();
