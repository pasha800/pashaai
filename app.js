async function checkManifestConnection(manifestUrl = state.settings.packManifestUrl) {
  const url = String(manifestUrl || DEFAULT_SETTINGS.packManifestUrl).trim();
  if (!canTryFetchManifest(url)) {
    updatePackProgress("fetch لە file:// کار ناکات.", 0, "تکایە localhost بەکاربهێنە یان دوگمەی پەکی ناوخۆیی بگرە.");
    toast("ئەمە بە file:// کراوەتەوە؛ دامەزراندنی پەکی ناوخۆیی بەکاربهێنە یان localhost.");
    return false;
  }
  updatePackProgress("پشکنینی manifest...", 5, url);
  try {
    const response = await fetch(url, { cache: "no-store" });
    if (!response.ok) throw new Error(`Manifest HTTP ${response.status}`);
    const manifest = await response.json();
    const chunks = Array.isArray(manifest.chunks) ? manifest.chunks : [];
    if (!chunks.length && !Array.isArray(manifest.knowledge)) throw new Error("Manifest چەند پارچەی chunks یان knowledge نییە.");
    if (chunks.length) {
      const firstUrl = resolvePackUrl(chunks[0].url || chunks[0].href || chunks[0].path, url);
      updatePackProgress("manifest باشە؛ پشکنینی یەکەم پارچە...", 35, firstUrl);
      const chunkResponse = await fetch(firstUrl, { cache: "no-store" });
      if (!chunkResponse.ok) throw new Error(`Chunk HTTP ${chunkResponse.status}`);
      try {
        if (chunkResponse.body && chunkResponse.body.getReader) {
          const reader = chunkResponse.body.getReader();
          await reader.read();
          await reader.cancel();
        } else {
          await chunkResponse.text();
        }
      } catch (streamError) {
        console.warn("Chunk stream check warning", streamError);
      }
    }
    updatePackProgress("manifest و CORS باشن.", 100, `${manifest.totalDocs || "?"} بابەت | ${chunks.length || 1} پارچە`);
    toast("پشکنین سەرکەوتوو بوو؛ manifest و پارچەکان دەخوێندرێنەوە.");
    return true;
  } catch (error) {
    console.warn("Manifest check failed", error);
    updatePackProgress("پشکنین سەرکەوتوو نەبوو.", 0, error.message || "Failed to fetch / CORS");
    toast("Manifest یان CORS کێشەی هەیە. ئەگەر remote ـە، Access-Control-Allow-Origin پێویستە.");
    return false;
  }
}

async function downloadAndInstallGeneralPack(manifestUrl = state.settings.packManifestUrl, options = {}) {
  if (state.generalInstallBusy) return;
  manifestUrl = String(manifestUrl || DEFAULT_SETTINGS.packManifestUrl).trim();
  const bundledManifest = isBundledManifestUrl(manifestUrl);

  if (!manifestUrl) {
    toast("لینکی manifest دیاری نەکراوە.");
    return;
  }

  if (!canTryFetchManifest(manifestUrl)) {
    if (bundledManifest || options.allowEmbeddedFallback) {
      return installEmbeddedGeneralPack({ force: options.force, quiet: options.quiet });
    }
    updatePackProgress("fetch لە file:// کار ناکات.", 0, "وێبسایتەکە بە localhost بکەرەوە یان manifest ـی https بەکاربهێنە.");
    toast("ئەمە بە file:// کراوەتەوە؛ بۆ manifest ـی دەرەکی localhost/https پێویستە.");
    return;
  }

  state.generalInstallBusy = true;
  updatePackProgress("پەیوەندی بە manifest ـەوە دەکرێت...", 2, manifestUrl);
  try {
    const manifestResponse = await fetch(manifestUrl, { cache: options.force ? "reload" : "default" });
    if (!manifestResponse.ok) throw new Error(`Manifest HTTP ${manifestResponse.status}`);
    const manifest = await manifestResponse.json();
    const packName = manifest.name || "General Knowledge Pack";
    const packVersion = String(manifest.version || "1");
    const installedKey = `${packName}:${packVersion}`;
    const current = await getGeneralStats();
    if (!options.force && current.installedKey === installedKey && current.count > 0) {
      state.generalStats = current;
      updatePackProgress("زانیاریی گشتی پێشتر دابەزێنراوە.", 100, `${current.count} بابەت | ${formatBytes(current.bytes)}`);
      renderGeneralPackStatus();
      return current;
    }

    await clearGeneralPack();
    const chunks = Array.isArray(manifest.chunks) ? manifest.chunks : [];
    if (!chunks.length && Array.isArray(manifest.knowledge)) {
      const result = await putGeneralDocsBatch(manifest.knowledge, installedKey);
      await setGeneralStats({ count: result.docs, bytes: JSON.stringify(manifest.knowledge).length, name: packName, version: packVersion, installedAt: nowIso(), status: "installed", installedKey, mode: "manifest-inline" });
      updatePackProgress("زانیاریی گشتی دابەزێنرا.", 100, `${result.docs} بابەت`);
      if (!options.quiet) toast("زانیاریی گشتی ئامادەی ئۆفلاینە.");
      return state.generalStats;
    }

    let totalDocs = 0;
    let downloadedBytes = 0;
    const totalBytes = Number(manifest.totalBytes || chunks.reduce((sum, c) => sum + Number(c.bytes || 0), 0));

    for (let i = 0; i < chunks.length; i++) {
      const chunk = chunks[i];
      const chunkUrl = resolvePackUrl(chunk.url || chunk.href || chunk.path, manifestUrl);
      const basePercent = (i / Math.max(1, chunks.length)) * 100;
      updatePackProgress(`دابەزاندنی پارچە ${i + 1}/${chunks.length}...`, basePercent, `${totalDocs} بابەت هەڵگیراوە`);
      const response = await fetch(chunkUrl, { cache: options.force ? "reload" : "default" });
      if (!response.ok) throw new Error(`Chunk HTTP ${response.status}: ${chunkUrl}`);
      const beforeBytes = downloadedBytes;
      const docsInChunk = await streamJsonlResponse(response, installedKey, (progress) => {
        const chunkBytes = progress.bytes || 0;
        const estimatedBytes = beforeBytes + chunkBytes;
        const percentByBytes = totalBytes ? Math.min(99, (estimatedBytes / totalBytes) * 100) : Math.min(99, basePercent + ((chunkBytes / Math.max(1, Number(chunk.bytes || 1))) * (100 / chunks.length)));
        updatePackProgress(`دابەزاندنی پارچە ${i + 1}/${chunks.length}...`, percentByBytes, `${totalDocs + (progress.docs || 0)} بابەت | ${formatBytes(estimatedBytes)} لە ${totalBytes ? formatBytes(totalBytes) : "قەبارەی نادیار"}`);
      });
      totalDocs += docsInChunk;
      downloadedBytes += Number(chunk.bytes || response.headers.get("content-length") || 0);
      if (!chunk.bytes) downloadedBytes = Math.max(downloadedBytes, beforeBytes);
    }

    await setGeneralStats({
      count: totalDocs,
      bytes: totalBytes || downloadedBytes,
      name: packName,
      version: packVersion,
      installedAt: nowIso(),
      status: "installed",
      installedKey,
      mode: "downloaded"
    });
    updatePackProgress("زانیاریی گشتی دابەزێنرا و ئێستا ئۆفلاینە.", 100, `${totalDocs} بابەت | ${formatBytes(totalBytes || downloadedBytes)}`);
    if (!options.quiet) toast("زانیاریی گشتی ئامادەیە و دەتوانرێت ئۆفلاین بەکاربهێنرێت.");
    return state.generalStats;
  } catch (error) {
    console.warn("General knowledge pack install failed", error);
    const quiet = options.quiet;
    if (bundledManifest || options.allowEmbeddedFallback) {
      updatePackProgress("fetch سەرکەوتوو نەبوو؛ fallback ـی ناوخۆیی بەکاردێت...", 20, error.message || "Failed to fetch");
      try {
        return await installEmbeddedGeneralPack({ force: options.force, quiet });
      } catch (fallbackError) {
        console.warn("Embedded fallback failed", fallbackError);
      }
    }
    updatePackProgress("زانیاریی گشتی دابەزێنرا نەکرا.", 0, error.message || "هەڵەیەک ڕوویدا");
    if (!quiet) toast("دابەزاندنی زانیاریی گشتی سەرکەوتوو نەبوو. لینکی manifest و CORS بپشکنە.");
    return null;
  } finally {
    state.generalInstallBusy = false;
    renderGeneralPackStatus();
  }
}


function normalizeWikiLang(lang) {
  const value = String(lang || "").trim().toLowerCase();
  if (!value) return "ckb";
  if (value === "ku" || value === "kur") return "ckb";
  return value.replace(/[^a-z0-9-]/g, "") || "ckb";
}

function wikiExtractToDoc(page, lang, sourcePack) {
  const title = String(page.title || "").trim();
  const extract = String(page.extract || "").replace(/\s+/g, " ").trim();
  if (!title || extract.length < 180) return null;
  const sentences = sentenceSplit(extract).slice(0, 3).join(" ") || extract.slice(0, 420);
  const langLabel = lang === "ckb" ? "کوردی" : lang === "ar" ? "عەرەبی" : lang === "en" ? "ئینگلیزی" : lang;
  return {
    id: `wiki-${lang}-${page.pageid || Math.abs(hashString(title + extract)).toString(36)}`,
    title,
    category: `زانیاریی گشتی / Wikipedia ${langLabel}`,
    question: `${title} چییە؟`,
    aliases: [title, `${title} چیست؟`, `what is ${title}`],
    answer: sentences,
    content: extract.slice(0, 18000),
    tags: ["زانیاریی گشتی", "Wikipedia", lang, title].filter(Boolean),
    priority: 1,
    sourcePack,
    learnedFrom: "internet-bootstrap",
    createdAt: nowIso(),
    updatedAt: nowIso()
  };
}

function hashString(text) {
  let h = 2166136261;
  for (let i = 0; i < String(text).length; i++) {
    h ^= String(text).charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

async function fetchWikipediaBatch(lang, batchSize, sourcePack) {
  const cleanLang = normalizeWikiLang(lang);
  const limit = Math.max(1, Math.min(20, Number(batchSize || 8)));
  const url = `https://${cleanLang}.wikipedia.org/w/api.php?origin=*&action=query&format=json&generator=random&grnnamespace=0&grnlimit=${limit}&prop=extracts|info&explaintext=1&exintro=0&inprop=url`;
  const response = await fetch(url, { cache: "no-store" });
  if (!response.ok) throw new Error(`Wikipedia ${cleanLang} HTTP ${response.status}`);
  const data = await response.json();
  const pages = Object.values(data?.query?.pages || {});
  return pages.map((page) => wikiExtractToDoc(page, cleanLang, sourcePack)).filter(Boolean);
}

async function safeInternetTargetBytes(requestedBytes) {
  const requested = Number(requestedBytes || DEFAULT_SETTINGS.internetBootstrapTargetBytes);
  try {
    if (navigator.storage?.estimate) {
      const estimate = await navigator.storage.estimate();
      const quota = Number(estimate.quota || 0);
      const used = Number(estimate.usage || 0);
      const available = Math.max(0, quota - used);
      if (quota && available) return Math.max(8 * 1024 * 1024, Math.min(requested, Math.floor(available * 0.72)));
    }
  } catch (error) {
    console.warn("Storage estimate unavailable", error);
  }
  return requested;
}

async function runInternetBootstrap(options = {}) {
  if (state.internetBootstrapBusy) return;
  if (location.protocol === "file:") {
    updatePackProgress("فێربوونی ئینتەرنێت بە file:// کار ناکات.", 0, "بۆ ئەمە پێویستە وێبسایتەکە لە localhost یان https بکرێتەوە.");
    if (!options.quiet) toast("بۆ فێربوونی ئینتەرنێت، وێبسایتەکە بە localhost بکەرەوە.");
    return;
  }
  if (!navigator.onLine) {
    updatePackProgress("ئینتەرنێت نییە.", 0, "کاتێک ئینتەرنێت هەبوو دووبارە هەوڵ بدە.");
    return;
  }

  state.internetBootstrapBusy = true;
  state.internetBootstrapStop = false;
  const sourcePack = `internet-bootstrap-${new Date().toISOString().slice(0, 10)}`;
  const requestedTarget = Number(state.settings.internetBootstrapTargetBytes || DEFAULT_SETTINGS.internetBootstrapTargetBytes);
  const targetBytes = await safeInternetTargetBytes(requestedTarget);
  const langs = String(state.settings.internetBootstrapLanguages || DEFAULT_SETTINGS.internetBootstrapLanguages)
    .split(/[ ,،]+/).map(normalizeWikiLang).filter(Boolean);
  const batchSize = Math.max(1, Math.min(20, Number(state.settings.internetBootstrapBatchSize || DEFAULT_SETTINGS.internetBootstrapBatchSize)));
  let stats = await getGeneralStats();
  let totalCount = Number(stats.count || 0);
  let totalBytes = Number(stats.bytes || 0);
  let addedCount = 0;
  let addedBytes = 0;
  let round = 0;
  const maxRounds = Number(options.maxRounds || 0);

  try {
    while (!state.internetBootstrapStop && totalBytes + addedBytes < targetBytes) {
      round += 1;
      if (maxRounds && round > maxRounds) break;
      let anySuccess = false;
      for (const lang of langs) {
        if (state.internetBootstrapStop || totalBytes + addedBytes >= targetBytes) break;
        const percent = targetBytes ? Math.min(99, ((totalBytes + addedBytes) / targetBytes) * 100) : 0;
        updatePackProgress("فێربوون لە ئینتەرنێت...", percent, `${totalCount + addedCount} بابەت | ${formatBytes(totalBytes + addedBytes)} لە ${formatBytes(targetBytes)} | زمان: ${lang}`);
        try {
          const docs = await fetchWikipediaBatch(lang, batchSize, sourcePack);
          if (!docs.length) continue;
          const result = await putGeneralDocsBatch(docs, sourcePack);
          const bytes = JSON.stringify(docs).length;
          addedCount += result.docs;
          addedBytes += bytes;
          anySuccess = true;
          await setGeneralStats({
            count: totalCount + addedCount,
            bytes: totalBytes + addedBytes,
            name: "Internet-learned General Knowledge",
            version: "web-bootstrap",
            installedAt: nowIso(),
            status: "installed",
            installedKey: sourcePack,
            mode: "internet-bootstrap"
          });
          await new Promise((resolve) => setTimeout(resolve, 180));
        } catch (error) {
          console.warn("Wikipedia bootstrap failed", lang, error);
        }
      }
      if (!anySuccess) throw new Error("هیچ سەرچاوەیەکی ئینتەرنێت نەخوێندرایەوە؛ CORS یان پەیوەندی بپشکنە.");
      await new Promise((resolve) => setTimeout(resolve, 240));
    }

    const stopped = state.internetBootstrapStop;
    updatePackProgress(
      stopped ? "فێربوونی ئینتەرنێت وەستا." : "فێربوونی ئینتەرنێت تەواو/سنووردار کرا.",
      Math.min(100, ((totalBytes + addedBytes) / Math.max(1, targetBytes)) * 100),
      `${totalCount + addedCount} بابەت | ${formatBytes(totalBytes + addedBytes)} هەڵگیراوە`
    );
    if (!options.quiet) toast(stopped ? "فێربوونی ئینتەرنێت وەستا." : "زانیاریی ئینتەرنێت هەڵگیرا و ئێستا ئۆفلاینە.");
  } catch (error) {
    console.warn("Internet bootstrap failed", error);
    updatePackProgress("فێربوونی ئینتەرنێت سەرکەوتوو نەبوو.", 0, error.message || "هەڵەی پەیوەندی/CORS");
    if (!options.quiet) toast("فێربوونی ئینتەرنێت سەرکەوتوو نەبوو؛ سەرچاوە، CORS و پەیوەندی بپشکنە.");
  } finally {
    state.internetBootstrapBusy = false;
    state.internetBootstrapStop = false;
    renderGeneralPackStatus();
  }
}

