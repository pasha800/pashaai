function lightStem(token) {
  let word = normalizeText(token);
  if (word.length <= 3) return word;

  const prefixes = ["دە", "ده", "نا", "بە", "به", "لە", "له", "بۆ", "بو", "وە", "وه"];
  for (const p of prefixes) {
    const np = normalizeText(p);
    if (word.startsWith(np) && word.length - np.length >= 3) {
      word = word.slice(np.length);
      break;
    }
  }

  const suffixes = [
    "ەکانیان", "ەکانتان", "ەکانمان", "ەکان", "کانیان", "کانتان", "کانمان", "کان", "ەکەیان", "ەکەتان", "ەکەمان", "ەکە", "ێک", "یک",
    "یەکان", "یەکە", "یان", "مان", "تان", "دا", "ەوە", "هەوە", "تر", "ترین", "ە", "ی"
  ];
  for (const s of suffixes) {
    const ns = normalizeText(s);
    if (word.endsWith(ns) && word.length - ns.length >= 3) {
      word = word.slice(0, -ns.length);
      break;
    }
  }
  return word || normalizeText(token);
}

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function baseTokens(text) {
  return normalizeText(text)
    .split(" ")
    .map((word) => word.trim())
    .filter((word) => word.length > 1 && !isStopWord(word))
    .flatMap((word) => unique([word, lightStem(word)]))
    .filter((word) => word.length > 1 && !isStopWord(word));
}

function tokenize(text, expand = true) {
  const tokens = baseTokens(text);
  if (!expand) return tokens;
  const expanded = [];
  for (const token of tokens) {
    expanded.push(token);
    const stem = lightStem(token);
    if (stem !== token) expanded.push(stem);
    const syns = SYNONYMS[token] || SYNONYMS[stem];
    if (syns) {
      syns.forEach((syn) => {
        baseTokens(syn).forEach((s) => expanded.push(s));
      });
    }
  }
  return unique(expanded);
}

function bigrams(tokens) {
  const out = [];
  for (let i = 0; i < tokens.length - 1; i++) out.push(`${tokens[i]} ${tokens[i + 1]}`);
  return out;
}

function trigrams(text) {
  const compact = normalizeText(text).replace(/\s+/g, "_");
  const grams = [];
  if (compact.length <= 3) return compact ? [compact] : [];
  for (let i = 0; i <= compact.length - 3; i++) grams.push(compact.slice(i, i + 3));
  return grams;
}

