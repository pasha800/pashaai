function openGeneralDB() {
  if (generalDbPromise) return generalDbPromise;
  if (!("indexedDB" in window)) {
    generalDbPromise = Promise.reject(new Error("IndexedDB is not supported in this browser."));
    return generalDbPromise;
  }
  generalDbPromise = new Promise((resolve, reject) => {
    const request = indexedDB.open(GENERAL_DB.name, GENERAL_DB.version);
    request.onupgradeneeded = () => {
      const db = request.result;
      if (!db.objectStoreNames.contains(GENERAL_DB.stores.meta)) {
        db.createObjectStore(GENERAL_DB.stores.meta, { keyPath: "key" });
      }
      if (!db.objectStoreNames.contains(GENERAL_DB.stores.docs)) {
        const docs = db.createObjectStore(GENERAL_DB.stores.docs, { keyPath: "id" });
        docs.createIndex("category", "category", { unique: false });
        docs.createIndex("sourcePack", "sourcePack", { unique: false });
      }
      if (!db.objectStoreNames.contains(GENERAL_DB.stores.postings)) {
        const postings = db.createObjectStore(GENERAL_DB.stores.postings, { keyPath: "key" });
        postings.createIndex("token", "token", { unique: false });
        postings.createIndex("docId", "docId", { unique: false });
      }
    };
    request.onsuccess = () => resolve(request.result);
    request.onerror = () => reject(request.error);
  });
  return generalDbPromise;
}

function txDone(tx) {
  return new Promise((resolve, reject) => {
    tx.oncomplete = resolve;
    tx.onerror = () => reject(tx.error);
    tx.onabort = () => reject(tx.error || new Error("IndexedDB transaction aborted"));
  });
}

async function idbGet(storeName, key) {
  const db = await openGeneralDB();
  return new Promise((resolve, reject) => {
    const request = db.transaction(storeName, "readonly").objectStore(storeName).get(key);
    request.onsuccess = () => resolve(request.result);
    request.onerror = () => reject(request.error);
  });
}

async function idbSet(storeName, value) {
  const db = await openGeneralDB();
  const tx = db.transaction(storeName, "readwrite");
  tx.objectStore(storeName).put(value);
  await txDone(tx);
}

