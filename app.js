function currentKnowledgeStamp() {
  return `${state.knowledge.length}:${state.knowledge.map((i) => `${i.id}:${i.updatedAt}:${i.priority}`).join("|")}`;
}

function buildIndex() {
  const stamp = currentKnowledgeStamp();
  if (cachedIndex && cachedIndexStamp === stamp) return cachedIndex;

  const documents = state.knowledge.map((item) => {
    const fields = weightedFields(item).map((field) => {
      const tokens = tokenize(field.text, false);
      const expanded = tokenize(field.text, true);
      return {
        ...field,
        norm: normalizeText(field.text),
        tokens,
        expanded,
        tokenFreq: frequencyMap(expanded),
        uniqueTokens: new Set(expanded),
        grams: frequencyMap(trigrams(field.text))
      };
    });
    const allTokens = fields.flatMap((field) => field.expanded);
    return {
      item,
      fields,
      tokens: allTokens,
      tokenFreq: frequencyMap(allTokens),
      uniqueTokens: new Set(allTokens),
      length: Math.max(1, allTokens.length),
      grams: frequencyMap(trigrams(knowledgeText(item))),
      norm: normalizeText(knowledgeText(item))
    };
  });

  const df = new Map();
  documents.forEach((doc) => {
    doc.uniqueTokens.forEach((token) => df.set(token, (df.get(token) || 0) + 1));
  });

  const avgLength = documents.length
    ? documents.reduce((sum, doc) => sum + doc.length, 0) / documents.length
    : 1;

  cachedIndex = { documents, df, avgLength, count: documents.length };
  cachedIndexStamp = stamp;
  return cachedIndex;
}

function idf(token, index) {
  const n = Math.max(1, index.count);
  const docFreq = index.df.get(token) || 0;
  return Math.log(1 + (n - docFreq + 0.5) / (docFreq + 0.5));
}

function bm25(term, freq, docLen, avgLen, index) {
  if (!freq) return 0;
  const k1 = 1.35;
  const b = 0.74;
  const denom = freq + k1 * (1 - b + b * (docLen / Math.max(1, avgLen)));
  return idf(term, index) * ((freq * (k1 + 1)) / denom);
}

function extractIntent(query) {
  const q = normalizeText(query);
  if (includesAny(q, ["چۆن", "هەنگاو", "ڕێنمایی", "step", "guide", "procedure", "how"])) return "steps";
  if (includesAny(q, ["نرخ", "تێچوو", "پارە", "price", "cost", "fee"])) return "cost";
  if (includesAny(q, ["کات", "ماوە", "کەی", "date", "time", "duration"])) return "time";
  if (includesAny(q, ["مەرج", "پێویست", "شرط", "requirement", "condition"])) return "requirements";
  if (includesAny(q, ["کورت", "پوختە", "خلاصە", "summary", "brief"])) return "brief";
  if (includesAny(q, ["وردەکاری", "زیاتر", "ڕوون", "detail", "explain"])) return "details";
  if (includesAny(q, ["پەیوەندی", "ژمارە", "ئیمەیل", "contact", "phone", "email"])) return "contact";
  return "general";
}

function enrichQueryWithContext(query) {
  if (!shouldUseConversationContext(query)) return query;

  const recent = [...state.chat].reverse();
  const lastUser = recent.find((msg) => msg.role === "user" && normalizeText(msg.text) !== normalizeText(query));
  const lastAssistant = recent.find((msg) => msg.role === "assistant" && Array.isArray(msg.sources) && msg.sources.length);
  const sourceWords = lastAssistant?.sources?.slice(0, 2).map((s) => s.item?.title || "").join(" ") || "";
  const context = [lastUser?.text || "", sourceWords].join(" ").trim();
  return context ? `${context} ${query}` : query;
}

function scoreDocument(query, doc, index) {
  const originalQuery = query;
  const qNorm = normalizeText(originalQuery);
  const qBase = tokenize(originalQuery, false);
  const qTokens = tokenize(originalQuery, true);
  const qBigrams = bigrams(qBase);
  const qGramFreq = frequencyMap(trigrams(originalQuery));
  const intent = extractIntent(originalQuery);
  const hits = [];
  const reasons = [];

  if (!qTokens.length) return { score: 0, hits: [], reasons: [] };

  let score = 0;
  const bm25Score = qTokens.reduce((sum, token) => {
    const freq = doc.tokenFreq.get(token) || 0;
    if (freq) hits.push(token);
    return sum + bm25(token, freq, doc.length, index.avgLength, index);
  }, 0);
  score += bm25Score * 18;

  const docGramCosine = cosineFromMaps(qGramFreq, doc.grams);
  score += docGramCosine * 30;
  if (docGramCosine > 0.34) reasons.push("دەقی نزیک");

  if (qNorm.length > 4 && doc.norm.includes(qNorm)) {
    score += 65;
    reasons.push("پرسیارەکە بە نزیکی هەمان دەقە");
  }

  const docWords = Array.from(doc.uniqueTokens);
  let fuzzyHits = 0;
  for (const token of qBase) {
    if (doc.uniqueTokens.has(token) || doc.uniqueTokens.has(lightStem(token))) continue;
    const best = docWords.some((word) => fuzzySimilarity(token, word) >= 0.78);
    if (best) {
      fuzzyHits += 1;
      hits.push(`${token}≈`);
    }
  }
  score += fuzzyHits * 5.5;

  for (const field of doc.fields) {
    if (!field.norm) continue;
    const fieldBm25 = qTokens.reduce((sum, token) => {
      const freq = field.tokenFreq.get(token) || 0;
      return sum + (freq ? Math.log(1 + freq) * field.weight : 0);
    }, 0);
    score += fieldBm25 * 3.1;

    if (qNorm.length > 4 && field.norm.includes(qNorm)) {
      score += 18 * field.weight;
      reasons.push(`${field.label}: هاوشێوەی ڕاستەوخۆ`);
    }

    for (const phrase of qBigrams) {
      if (phrase.length > 3 && field.norm.includes(phrase)) {
        score += 7.5 * field.weight;
        hits.push(phrase);
        reasons.push(`${field.label}: دەستەواژەی گونجاو`);
      }
    }
  }

  const uniqueHits = unique(hits).slice(0, 12);
  const meaningfulQ = unique(qBase);
  const coverage = meaningfulQ.length ? Math.min(1, uniqueHits.length / meaningfulQ.length) : 0;
  score *= (0.72 + coverage * 0.55);

  const profile = analyzeQuery(originalQuery);
  // ڕێگری لە هەڵەی گەورە: پرسیاری کورت و پێناسەیی وەک «ئاو چیە» نابێت بە دەقی درێژی نامەربووت وەڵام بدرێتەوە.
  if ((profile.isDefinition || profile.isWho) && profile.isShort && !hasDirectSubjectMatch(originalQuery, doc.item)) {
    score *= 0.025;
    if (coverage < 0.75) score = Math.min(score, 9);
  }

  if (intent !== "general") {
    const intentHits = tokenize(intent, true).some((token) => doc.uniqueTokens.has(token));
    if (intentHits) score += 6;
  }

  score += Math.min(3, Number(doc.item.priority || 1)) * 4.5;
  if ((doc.item.tags || []).some((tag) => qNorm.includes(normalizeText(tag)))) score += 12;

  return {
    score,
    hits: uniqueHits,
    reasons: unique(reasons).slice(0, 4),
    coverage,
    gramSimilarity: docGramCosine
  };
}

function searchKnowledge(query, limit = 5, useContext = true) {
  const enriched = useContext ? enrichQueryWithContext(query) : query;
  const index = buildIndex();
  return index.documents
    .map((doc) => ({ item: doc.item, ...scoreDocument(enriched, doc, index), query: enriched }))
    .filter((row) => row.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit);
}

function confidenceFromScore(score, coverage = 0) {
  if (score >= 110 && coverage >= 0.45) return "زۆر بەرز";
  if (score >= 68 && coverage >= 0.28) return "بەرز";
  if (score >= 34) return "مامناوەند";
  return "کەم";
}

function confidencePercent(score, coverage = 0) {
  const raw = Math.round(Math.min(96, Math.max(18, score * 0.62 + coverage * 35)));
  return raw;
}

