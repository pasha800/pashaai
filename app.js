function analyzeQuery(query) {
  const norm = normalizeText(query);
  const base = unique(tokenize(query, false));
  const expanded = unique(tokenize(query, true));
  const isDefinition = includesAny(norm, DEFINITION_MARKERS) || /\b(چیە|چییە|چییه|چیه)\b/.test(norm);
  const isWho = includesAny(norm, ["کێیە", "کێیه", "کێ بوو", "who is"]);
  const isHow = includesAny(norm, ["چۆن", "how", "هەنگاو"]);
  const explicitFollowup = includesAny(norm, FOLLOWUP_INTENT_WORDS) || includesAny(query, FOLLOWUP_WORDS);
  const isShort = base.length <= 3;
  const subject = extractDefinitionSubject(query, base);
  return { norm, base, expanded, isDefinition, isWho, isHow, explicitFollowup, isShort, subject };
}

function extractDefinitionSubject(query, precomputedBase) {
  const norm = normalizeText(query);
  let cleaned = norm;
  DEFINITION_MARKERS.forEach((marker) => {
    cleaned = cleaned.replaceAll(normalizeText(marker), " ");
  });
  cleaned = cleaned.replace(/\b(چیە|چییە|چییه|چیه|کێیە|کێیه|واتە|مانا|پێناسە)\b/g, " ");
  const tokens = unique((precomputedBase && precomputedBase.length ? precomputedBase : baseTokens(cleaned))
    .filter((token) => !isStopWord(token)));
  return tokens.slice(0, 4).join(" ");
}

