function humanIntro(confidence, intent) {
  const style = state.settings.style || "human";
  if (style === "formal") {
    if (confidence === "کەم") return "بەپێی زانیارییە بەردەستەکان، ئەمە نزیکترین وەڵامە:";
    return "بەپێی بنکەی زانیاریی فێرکراو، وەڵامەکە بەم شێوەیەیە:";
  }
  if (style === "brief" || intent === "brief") return "بە کورتی:";
  if (confidence === "زۆر بەرز") return "بەڵێ، ئەمە زانیارییەکەیە:";
  if (confidence === "بەرز") return "باشە، ئەوەی لە زانیارییەکاندا هەیە ئەمەیە:";
  if (confidence === "مامناوەند") return "وەڵامێکی نزیکم دۆزیەوە، بەڵام باشترە سەرچاوەکەش ببینیت:";
  return "دڵنیاییەکەم تەواو بەرز نییە، بەڵام نزیکترین زانیاری ئەمەیە:";
}

function cleanAnswerText(text) {
  return String(text || "")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

function makeAnswerBody(query, top, matches) {
  const item = top.item;
  const intent = extractIntent(query);
  const confidence = confidenceFromScore(top.score, top.coverage);
  const intro = humanIntro(confidence, intent);
  const answerSentences = sentenceSplit(item.answer);
  const contentSentences = sentenceSplit(item.content);
  const wantsDetails = ["details", "steps", "requirements", "contact", "time", "cost"].includes(intent);

  let body = cleanAnswerText(item.answer);
  if (!body && item.content) body = cleanAnswerText(item.content);

  if (intent === "brief" && answerSentences.length > 2) {
    body = answerSentences.slice(0, 2).join(" ");
  }

  if (wantsDetails && item.content && normalizeText(item.content) !== normalizeText(item.answer)) {
    body += `\n\nوردەکاریی زیاتر:\n${cleanAnswerText(item.content)}`;
  } else if (contentSentences.length && item.content && normalizeText(item.content) !== normalizeText(item.answer)) {
    const usefulExtra = contentSentences.slice(0, 2).join(" ");
    if (usefulExtra && !normalizeText(body).includes(normalizeText(usefulExtra).slice(0, 30))) {
      body += `\n\nتێبینیی پەیوەندیدار: ${usefulExtra}`;
    }
  }

  const extras = matches
    .slice(1, Math.max(2, Number(state.settings.maxSources || 4)))
    .filter((m) => m.item.id !== item.id && m.score >= Math.max(18, top.score * 0.38))
    .slice(0, 2);

  if (extras.length && intent !== "brief") {
    const extraText = extras.map((m) => `- ${m.item.title}: ${String(m.item.answer || m.item.content).slice(0, 190)}${String(m.item.answer || m.item.content).length > 190 ? "..." : ""}`).join("\n");
    body += `\n\nبابەتی نزیک/پەیوەندیدار:\n${extraText}`;
  }

  const hitText = top.hits?.length ? ` وشە/دەستەواژەی گونجاو: ${top.hits.slice(0, 6).join("، ")}.` : "";
  const confidenceLine = `\n\nدڵنیایی: ${confidence} (${confidencePercent(top.score, top.coverage)}%).${hitText}`;

  return `${intro}\n\n${body}${confidenceLine}`;
}

function clarificationText(query, matches) {
  const options = matches.slice(0, 4).map((m, idx) => `${idx + 1}. ${m.item.title}${m.item.category ? ` ـ ${m.item.category}` : ""}`).join("\n");
  return `لە بنکەی زانیاریدا چەند بابەتی نزیکم دۆزیەوە، بەڵام پرسیارەکە بە تەواوی ڕوون نییە.\n\nمەبەستت یەکێک لەمانەیە؟\n${options}\n\nدەتوانیت پرسیارەکە بە وشەی وردتر بنووسیت، یان ناوی بابەتەکە زیاد بکەیت.`;
}

async function buildAnswer(query) {
  if (isGreetingOnly(query)) {
    return {
      text: `سڵاو، بەخێربێیت. من ${state.settings.botName}م و بە تەواوی ئۆفلاین کار دەکەم. پرسیارەکەت بنووسە؛ هەوڵ دەدەم لەسەر زانیاریی فێرکراو و زانیاریی گشتیی هەڵگیراو وەڵامێکی ڕوون بدەمەوە.`,
      matches: [],
      confidence: "گفتوگۆ"
    };
  }
  if (isThanksOnly(query)) {
    return { text: "بەخێر بێیت. هەر پرسیارێکی ترت هەبوو، بنووسە.", matches: [], confidence: "گفتوگۆ" };
  }
  if (isGoodbyeOnly(query)) {
    return { text: "خوات لەگەڵ. کاتێک پێویستت بوو دەتوانیت بگەڕێیتەوە و پرسیار بکەیت.", matches: [], confidence: "گفتوگۆ" };
  }
  if (wantsSystemKnowledge(query)) {
    return { text: summarizeKnowledge(), matches: [], confidence: "بەرز" };
  }

  const localLimit = Math.max(6, Number(state.settings.maxSources || 4) + 2);
  const useContext = shouldUseConversationContext(query);
  const profile = analyzeQuery(query);
  const localMatches = searchKnowledge(query, localLimit, useContext).map((m) => ({ ...m, sourceType: "admin" }));
  const coreMatches = searchCoreKnowledge(query, localLimit);
  const generalMatches = await searchGeneralKnowledge(query, localLimit);

  // پێناسەی بنەڕەتی لە یەک وشەدا: core/general direct دەبێت پێش دەقەکانی بەڕێوەبەر بێت،
  // چونکە بەڕێوەبەر دەتوانێت هەواڵ یان دەقی درێژ دابنێت کە وشەیەکی گشتی تێدایە.
  const directCore = keepOnlyDirectDefinitionMatches(query, coreMatches);
  const directGeneral = keepOnlyDirectDefinitionMatches(query, generalMatches);
  const directLocal = keepOnlyDirectDefinitionMatches(query, localMatches);
  if (isBasicDefinitionQuery(query) && state.settings.preferGeneralForDefinitions !== false) {
    const directPool = [...directCore, ...directGeneral, ...directLocal]
      .map((m) => ({ ...m, score: m.score + (m.sourceType === "core" ? 260 : m.sourceType === "general" ? 120 : 40) }))
      .sort((a, b) => b.score - a.score);
    if (directPool.length) {
      const directStrong = directPool.slice(0, Math.max(1, Number(state.settings.maxSources || 4)));
      return {
        text: makeAnswerBody(query, directStrong[0], directStrong),
        matches: directStrong,
        confidence: confidenceFromScore(directStrong[0].score, Math.max(directStrong[0].coverage || 0, 0.9))
      };
    }
  }

  const matches = sourceAdjustedMatches(query, localMatches, coreMatches, generalMatches)
    .slice(0, Math.max(localLimit, Number(state.settings.maxSources || 4) + 2));

  const threshold = Number(state.settings.threshold || DEFAULT_SETTINGS.threshold);
  const effectiveThreshold = (profile.isDefinition || profile.isWho) ? Math.max(10, threshold * 0.55) : threshold;
  const strongMatches = matches.filter((m) => m.score >= effectiveThreshold && (!(profile.isDefinition || profile.isWho) || m.coverage >= 0.25 || hasDirectSubjectMatch(query, m.item)));
  const top = matches[0];

  if (!top) {
    const packHint = state.generalStats?.count
      ? "زانیاریی گشتی هەڵگیراوە، بەڵام هیچ بابەتێکی گونجاو نەدۆزرایەوە."
      : "دەتوانیت لە ڕێکخستنەکان Knowledge Pack ـی زانیاریی گشتی دابەزێنیت تا پرسیارە گشتییەکان باشتر وەڵام بدرێنەوە.";
    return {
      text: `ببورە، لە بنکەی زانیاریی مندا هیچ زانیارییەکی پەیوەندیدار نەدۆزرایەوە.

${packHint}

بۆ زانیاریی تایبەتی، باشترین چارە ئەوەیە بەڕێوەبەر ئەم بابەتە لە بەشی «فێرکردنی زیرەکی» زیاد بکات.`,
      matches: [],
      confidence: "نەناسراو"
    };
  }

  if (!strongMatches.length) {
    const canClarify = state.settings.askClarification !== false && matches.length >= 2 && top.score >= Math.max(12, threshold * 0.48);
    if (canClarify) {
      return { text: clarificationText(query, matches), matches, confidence: "کەم" };
    }
    return {
      text: `ببورە، وەڵامێکی دڵنیا بۆ ئەم پرسیارە نییە. نزیکترین بابەت کە دۆزیمەوە «${top.item.title}» بوو، بەڵام گونجانەکە بەشی پێویست نییە.

بۆ وەڵامی ڕاستتر، بەڕێوەبەر با پرسیارە هاوشێوەکان، تاگەکان، و وەڵامی تەواوتر زیاد بکات؛ یان Knowledge Pack ـی گشتی نوێتر دابەزێنێت.`,
      matches,
      confidence: "کەم"
    };
  }

  const answerText = makeAnswerBody(query, strongMatches[0], strongMatches.length > 1 ? strongMatches : matches);
  return {
    text: answerText,
    matches: strongMatches,
    confidence: confidenceFromScore(strongMatches[0].score, strongMatches[0].coverage)
  };
}

