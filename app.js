function hasDirectSubjectMatch(query, item) {
  const profile = analyzeQuery(query);
  const subject = profile.subject || profile.base.join(" ");
  if (!subject) return false;
  const directText = normalizeText(titleQuestionAliasText(item));
  const subjectTokens = unique(tokenize(subject, true));
  if (normalizeText(item.title) === normalizeText(subject)) return true;
  if (normalizeText(item.question).includes(normalizeText(query))) return true;
  return subjectTokens.length > 0 && subjectTokens.every((token) => directText.includes(token));
}

function directSubjectStrength(query, item) {
  const profile = analyzeQuery(query);
  const subject = normalizeText(profile.subject || profile.base.join(" "));
  if (!subject) return 0;
  const title = normalizeText(item.title || "");
  const question = normalizeText(item.question || "");
  const aliases = (item.aliases || []).map((a) => normalizeText(a));
  const direct = normalizeText(titleQuestionAliasText(item));
  if (title && title === subject) return 1;
  if (aliases.some((a) => a === subject || a === normalizeText(query))) return 0.96;
  if (question && question === normalizeText(query)) return 0.94;
  const subjectTokens = unique(tokenize(subject, true));
  if (subjectTokens.length && subjectTokens.every((token) => direct.split(" ").includes(token))) return 0.9;
  if (subjectTokens.length && subjectTokens.every((token) => direct.includes(token))) return 0.78;
  return 0;
}

