function isBundledManifestUrl(manifestUrl = state.settings.packManifestUrl) {
  const raw = String(manifestUrl || "").trim() || DEFAULT_SETTINGS.packManifestUrl;
  if (raw === DEFAULT_SETTINGS.packManifestUrl) return true;
  try {
    const url = new URL(raw, location.href);
    const bundled = new URL(DEFAULT_SETTINGS.packManifestUrl, location.href);
    return url.href === bundled.href || url.pathname.endsWith("/knowledge-pack/manifest.json");
  } catch {
    return raw.endsWith("knowledge-pack/manifest.json");
  }
}

function canTryFetchManifest(manifestUrl) {
  if (location.protocol === "file:") return false;
  const raw = String(manifestUrl || "").trim();
  if (!raw) return true;
  try {
    const url = new URL(raw, location.href);
    if (url.protocol === "file:") return false;
    return ["http:", "https:"].includes(url.protocol);
  } catch {
    return location.protocol !== "file:";
  }
}

