function importData(file) {
  const reader = new FileReader();
  reader.onload = () => {
    try {
      const parsed = JSON.parse(reader.result);
      const incoming = Array.isArray(parsed) ? parsed : parsed.knowledge;
      if (!Array.isArray(incoming)) throw new Error("Invalid knowledge file");
      const clean = normalizeImportedKnowledge(incoming).filter((item) => item.question && item.answer);
      const existingIds = new Set(state.knowledge.map((i) => i.id));
      state.knowledge = [...clean.filter((i) => !existingIds.has(i.id)), ...state.knowledge];
      cachedIndex = null;
      save(STORE.knowledge, state.knowledge);
      renderKnowledge();
      renderDashboard();
      toast(`${clean.length} زانیاری هاوردەکرا.`);
    } catch (error) {
      toast("فایلی هاوردەکردن دروست نییە.");
    }
  };
  reader.readAsText(file);
}

function guessTags(text) {
  const freq = frequencyMap(tokenize(text, true));
  return [...freq.entries()]
    .sort((a, b) => b[1] - a[1])
    .map(([token]) => token)
    .filter((token) => token.length > 2)
    .slice(0, 12);
}

function smartFill() {
  const text = $("#quickText").value.trim();
  if (!text) return toast("تکایە تێکستێک بنووسە.");
  const sentences = sentenceSplit(text);
  const title = (sentences[0] || text).slice(0, 90).replace(/[.؟!؛]+$/, "") || "زانیاری نوێ";
  const questionCandidate = sentences.find((s) => s.includes("؟") || includesAny(s, ["چۆن", "چی", "کەی", "بۆچی", "how", "what", "when"])) || `دەربارەی ${title} چی دەزانیت؟`;
  const tags = guessTags(text);
  const aliases = [
    questionCandidate.replace(/[؟?]/g, "").trim() + "؟",
    `${title} چییە؟`,
    `زانیاری دەربارەی ${title}`
  ];

  $("#titleInput").value = title;
  $("#questionInput").value = aliases[0];
  if ($("#aliasesInput")) $("#aliasesInput").value = unique(aliases).join("\n");
  $("#answerInput").value = sentences.slice(0, 4).join(" ") || text;
  $("#contentInput").value = text;
  $("#tagsInput").value = tags.join(", ");
  toast("فۆرم بە شێوەی خۆکار پڕکرایەوە؛ پێش پاشەکەوت پێداچوونەوەی بکە.");
}

