function lineToKnowledge(line) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith("#")) return null;
  const parsed = JSON.parse(trimmed);
  if (parsed.knowledge && Array.isArray(parsed.knowledge)) return parsed.knowledge;
  return parsed;
}

async function processJsonlText(text, sourcePack, updateProgress) {
  const lines = String(text || "").split(/\r?\n/);
  let batch = [];
  let docs = 0;
  for (const line of lines) {
    const row = lineToKnowledge(line);
    if (!row) continue;
    if (Array.isArray(row)) batch.push(...row);
    else batch.push(row);
    if (batch.length >= 80) {
      const result = await putGeneralDocsBatch(batch, sourcePack);
      docs += result.docs;
      batch = [];
      if (updateProgress) updateProgress({ docs });
      await new Promise((resolve) => setTimeout(resolve, 0));
    }
  }
  if (batch.length) {
    const result = await putGeneralDocsBatch(batch, sourcePack);
    docs += result.docs;
    if (updateProgress) updateProgress({ docs });
  }
  return docs;
}

async function streamJsonlResponse(response, sourcePack, updateProgress) {
  if (!response.body || !response.body.getReader) {
    return processJsonlText(await response.text(), sourcePack, updateProgress);
  }
  const reader = response.body.getReader();
  const decoder = new TextDecoder();
  let buffer = "";
  let batch = [];
  let docs = 0;
  let bytes = 0;

  while (true) {
    const { value, done } = await reader.read();
    if (done) break;
    bytes += value.byteLength;
    buffer += decoder.decode(value, { stream: true });
    const parts = buffer.split(/\r?\n/);
    buffer = parts.pop() || "";
    for (const line of parts) {
      const row = lineToKnowledge(line);
      if (!row) continue;
      if (Array.isArray(row)) batch.push(...row);
      else batch.push(row);
      if (batch.length >= 80) {
        const result = await putGeneralDocsBatch(batch, sourcePack);
        docs += result.docs;
        batch = [];
        if (updateProgress) updateProgress({ docs, bytes });
        await new Promise((resolve) => setTimeout(resolve, 0));
      }
    }
    if (updateProgress) updateProgress({ docs, bytes });
  }
  buffer += decoder.decode();
  if (buffer.trim()) {
    const row = lineToKnowledge(buffer);
    if (Array.isArray(row)) batch.push(...row);
    else if (row) batch.push(row);
  }
  if (batch.length) {
    const result = await putGeneralDocsBatch(batch, sourcePack);
    docs += result.docs;
  }
  if (updateProgress) updateProgress({ docs, bytes });
  return docs;
}

