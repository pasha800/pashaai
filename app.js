async function putGeneralDocsBatch(items, sourcePack, onCount) {
  const clean = items.map((item) => canonicalKnowledgeItem(item, sourcePack)).filter(Boolean);
  if (!clean.length) return { docs: 0, postings: 0 };

  const db = await openGeneralDB();
  const tx = db.transaction([GENERAL_DB.stores.docs, GENERAL_DB.stores.postings], "readwrite");
  const docStore = tx.objectStore(GENERAL_DB.stores.docs);
  const postingStore = tx.objectStore(GENERAL_DB.stores.postings);
  let postings = 0;

  for (const item of clean) {
    const text = indexableTextForGeneralDoc(item);
    const tokenEntries = topTokenEntries(text);
    const compactDoc = {
      ...item,
      searchPreview: text.slice(0, 22000),
      tokenCount: tokenEntries.reduce((sum, [, tf]) => sum + tf, 0),
      indexedTokens: tokenEntries.map(([token]) => token),
      importedAt: nowIso()
    };
    docStore.put(compactDoc);
    for (const [token, tf] of tokenEntries) {
      postingStore.put({
        key: `${token}|${item.id}`,
        token,
        docId: item.id,
        tf,
        priority: Math.min(3, Number(item.priority || 1))
      });
      postings += 1;
    }
  }
  await txDone(tx);
  if (onCount) onCount(clean.length, postings);
  return { docs: clean.length, postings };
}

